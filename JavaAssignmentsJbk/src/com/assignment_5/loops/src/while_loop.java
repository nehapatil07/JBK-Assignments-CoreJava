package com.assignment_5.loops.src;

public class while_loop {
public static void main(String[] args) {
	int i=1; //Initialization
	while(i<5){  //condition is written inside while loop
		System.out.println(i);  //i will print
		i++; //increment the value of i
	}
}
}
//for and while loop do the same work. the difference between them is syntax.