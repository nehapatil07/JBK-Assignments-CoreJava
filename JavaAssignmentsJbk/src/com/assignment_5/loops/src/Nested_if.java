package com.assignment_5.loops.src;

public class Nested_if {
	public static void main(String[] args) {
		int x=10;
		int y=10;
		if (x==10){
			if (y==10){
				System.out.println("both numbers are equal");
			}
		else {
			System.out.println("both numbers are not equal");
		}
	}}

}
